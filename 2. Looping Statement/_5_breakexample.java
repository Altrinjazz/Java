public class _5_breakexample {
    public static void main(String[] args) {
        for(int i=1;i<=10;i++){  
            if(i==5){  
                break;  
            }  
        System.out.println(i);
        } 
        System.out.println();
        System.out.println("5. Break Statement");
        System.out.println();
        System.out.println("When a break statement is encountered inside a loop, the loop is immediately terminated and the program control resumes at the next statement following the loop.");   
    }    
}
