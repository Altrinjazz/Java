public class _3_for_loop {
    public static void main(String[] args) {
        int i,sum=0;
        for(i=1;i<=10;i++)
        {
            sum=sum+1;
        }
        System.out.println("output is "+sum);
        System.out.println();
        System.out.println("3. For Loop");
        System.out.println();
        System.out.println(" for loop is known as entry check loop because the condition is checked at the begining of the body of the loop.");
   }
}
