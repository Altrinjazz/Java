public class _1_while_loop {
    public static void main(String[] args) {
        int i=1;
        int sum=0;
        while (i<=10) {
            sum=sum++;
        }
        System.out.println("output is "+sum);
        System.out.println();
        System.out.println("1. While Loop");
        System.out.println();
        System.out.println("While loop is know as entry check loop because the condition is checked at the beginning of the body of the loop.");
    }
}
